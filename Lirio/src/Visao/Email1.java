    private void jButtonanexoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonanexoActionPerformed
        JFileChooser file = new JFileChooser();
        file.setMultiSelectionEnabled(true);
        file.setFileSelectionMode(JFileChooser.FILES_ONLY);
        int i = file.showOpenDialog(null);
        if (i == 1) {
            jTextFieldanexo.setText("");
            Anexo = null;
        } else {
            Anexo = file.getSelectedFiles();

            String SomaAnexo1 = "";
            String SomaAnexo2 = "";
            for (File enderec : Anexo) {
                jTextFieldanexo.setText(enderec.getPath());
                SomaAnexo1 = jTextFieldanexo.getText();
                SomaAnexo2 = SomaAnexo2 + SomaAnexo1 + ";";
                jTextFieldanexo.setText(SomaAnexo2);

            }
        }
    }//GEN-LAST:event_jButtonanexoActionPerformed

