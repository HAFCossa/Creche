     void inicializar() {
            f= new Crianca();
            f.setNome(jTextFieldnome.getText().toString().trim());
            f.setApelido(jTextFieldapelido.getText().toString().trim());
            f.setNascimento(jTextFieldnascimento.getText().toString().trim());
            f.setMorada(jTextFieldmorada.getText().toString().trim());
            f.setNacionalidade(jTextFieldnacionalidade.getText().toString().trim());
            f.setNaturalidade(jTextFieldnaturalidade.getText().toString().trim());
            f.setTelefone(jTextFieldtelefone.getText().toString().trim());
            f.setEmail(jTextFieldemail.getText().toString().trim());
            f.setNomepai(jTextFieldprofissaopai.getText().toString().trim());
            f.setProfissaopai(jTextFieldprofissaopai.getText().toString().trim());
            f.setNomemae(jTextFieldnomemae.getText().toString().trim());
            f.setProfissaomae(jTextFieldprofissaomae.getText().toString().trim());
            f.setGenero(jComboBoxdoenca.getSelectedItem().toString().trim());
            if(jRadioButtonfeminino.isSelected())
            f.setGenero(jRadioButtonfeminino.getText().toString().trim());
            if(jRadioButtonmasculino.isSelected())
            f.setGenero(jRadioButtonmasculino.getText().toString().trim());
            f.setDoenca(jComboBoxdoenca.getSelectedItem().toString().trim());
    }

    void mostrarTabela() {
      mod=(DefaultTableModel) tabela.getModel();
       
      
         for(Crianca i: CriancaDao.buscar()) {
         Object[] objecto= {i.getId(),i.getApelido(),i.getNome(),i.getNascimento(),i.getMorada(),i.getNacionalidade(),i.getGenero(),
        i.getTelefone(),i.getEmail(),i.getNomepai(),i.getProfissaopai(),i.getNomemae(),i.getProfissaomae(),i.getDoenca()
            }; 
          
          mod.addRow(objecto);
   

  }
}

    private void inicializarEvento() {
            s= new Evento();
            s.setDataevento(jTextFielddata.getText().trim());
            s.setDescricao(jTextAreaDescricao.getText().toString().trim());
            s.setHoraevento((Integer.parseInt(jTextFieldhora.getText().trim())));
            s.setLocalevento((jTextFieldLocalEvento.getText().toString().trim()));
            s.setTipoevento(jComboBoxevento.getSelectedItem().toString().trim());
        
    }

